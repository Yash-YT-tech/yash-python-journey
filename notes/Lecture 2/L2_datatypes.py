# Datatypes
a = 1  # a is an integer

b = 5.22  # b is a floating point number

c = "Harry"  # c is a string

d = False  # d is a boolean variable

e = None  # e is a none type variable

# ==============================
# PYTHON DATA TYPES â€” QUICK NOTES
# ==============================

# Data types define what kind of value a variable stores.
# Python is dynamically typed -> no need to declare type manually.

# ------------------------------
# 1. NUMERIC DATA TYPES
# ------------------------------

# int -> whole numbers
a = 10          # example of integer

# float -> decimal numbers
b = 5.22        # example of float

# complex -> numbers with imaginary part
z = 2 + 3j      # complex number


# ------------------------------
# 2. STRING (str)
# ------------------------------

# Used to store text
c = "Harry"     # string example

# Strings can use:
# "double quotes"
# 'single quotes'
# """triple quotes"""


# ------------------------------
# 3. BOOLEAN (bool)
# ------------------------------

# Only two values: True or False
d = False       # boolean example


# ------------------------------
# 4. NONE TYPE
# ------------------------------

# None means no value / empty variable
e = None


# ------------------------------
# 5. COLLECTION DATA TYPES
# ------------------------------

# list -> ordered and changeable (mutable)
my_list = [1, 2, 3]

# tuple -> ordered but not changeable (immutable)
my_tuple = (1, 2, 3)

# set -> unordered and no duplicates
my_set = {1, 2, 3}

# dictionary -> key-value pairs
my_dict = {"name": "Yash", "age": 17}


# ------------------------------
# HOW TO CHECK DATA TYPE
# ------------------------------

print(type(a))   # shows datatype of variable


# ==============================
# POINTS TO REMEMBER
# ==============================

# int, float, complex -> numeric types
# str -> text
# bool -> logical values
# None -> absence of value
# list -> mutable
# tuple -> immutable
# set -> unique items
# dict -> key:value data